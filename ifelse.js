let marks = 76;

if (marks >= 90 && marks <= 100) {
  console.log("Grade: A");
} else if (marks >= 75 && marks <= 89) {
   console.log("Grade: B");
} else if (marks >= 50 && marks <= 74) {
  console.log("Grade: C");
} else if (marks >= 0 && marks < 50) {
   console.log("Grade: Fail");
} else {
   console.log("Invalid marks entered.");
}
